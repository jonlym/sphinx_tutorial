"""
Useful for dealing with two numbers
"""

def add_numbers(num1, num2):
	"""Adds two numbers.

	Args:
		num1 (float): First number to add
		num2 (float): Second number to add

	Returns:
		float: Sum of the two numbers
	"""
	return num1+num2

def prod_numbers(num1, num2):
	"""Takes the product of two numbers.

	Args:
		num1 (float): First number to multiply
		num2 (float): Second number to multiply

	Returns:
		float: Product of the two numbers
	"""
	return num1*num2

